function res = hermite(x, y, y1, p)
A = (1 + 2.*(p - x(1))./ (x(2) - x(1))).*(((p - x(2))./(x(1) - x(2))).^2);
B = (1 + 2.*(p - x(2))./ (x(1) - x(2))).*(((p - x(1))./(x(2) - x(1))).^2);
C = (p - x(1)).*(((p - x(2)) ./ (x(1) - x(2))).^2);
D = (p - x(2)).*(((p - x(1)) ./ (x(2) - x(1))).^2);
res =  A.*y(1) + B .*y(2) + C.*y1(1) + D.*y1(2);
end